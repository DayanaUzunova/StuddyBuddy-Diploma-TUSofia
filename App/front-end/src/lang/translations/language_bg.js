export function language_bg(word) {
    switch (word) {
        case 'login':
            return 'Вход';
        case 'your_pers_learning_companion':
            return 'Твоят личен помощник в ученето';
        case 'what_you_get':
            return 'Какво получаваш';
        case 'organize_and_study':
            return 'Организирай ученето си, чати с ученици или учители и се забавлявай, докато учиш.';
        case 'view_profile':
            return 'Прегледай си профила'
    }
}